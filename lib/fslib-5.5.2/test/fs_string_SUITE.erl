%%====================================================================
%% Test Cases
%%====================================================================

test_binary_to_hex (doc)   -> [];
test_binary_to_hex (suite) -> [];
test_binary_to_hex (Config) when list (Config) ->
    ?MATCH ("00ff", fs_string:binary_to_hex (<<0, 255>>)),
    Config.
