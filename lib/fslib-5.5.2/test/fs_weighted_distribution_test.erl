%%--------------------------------------------------------------------
%% Macros
%%--------------------------------------------------------------------

-define (FILE_NAME, "test_terms.txt").

%%--------------------------------------------------------------------
%% Records
%%--------------------------------------------------------------------

%%====================================================================
%% External functions
%%====================================================================

%%--------------------------------------------------------------------
%% @doc All the starting point for the test functions.
%% @spec all() -> ok | exit(Reason)
%% @end 
%%--------------------------------------------------------------------

all (Config) ->
    test_output_term ().

%%====================================================================
%% Test Cases
%%====================================================================

test_output_term () ->
    create_file (?FILE_NAME),
    State   = fs_weighted_distribution:read_file (?FILE_NAME),
    bloobin = fs_weighted_distribution:output_term (State),
    file:delete (?FILE_NAME).

%%====================================================================
%% Internal functions
%%====================================================================

create_file (FileName) ->
    {ok, IODevice} = file:open (FileName, [write]),
    ok=file:write (IODevice, "{0, zubin}.\n"),
    ok=file:write (IODevice, "{1, bloobin}.\n"),
    file:close (IODevice).
